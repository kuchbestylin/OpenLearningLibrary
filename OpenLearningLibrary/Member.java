
public class Member {
	private String fName;
	private String sName;
	private String homeAddress;
	private String dOB;
	private String email;
	private String password;
	
	public Member(String fName, String sName, String homeAddress, String dOB, String email, String password) {
		this.fName = fName;
		this.sName = sName;
		this.homeAddress = homeAddress;
		this.dOB = dOB;
		this.email = email;
		this.password = password;
	}
	
}
